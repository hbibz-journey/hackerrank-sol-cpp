#include <iostream>
using namespace std;

// main function
int main() {
    // defining sum var and initializing it with zero
    int sum = 0;
    // inititalizing variables to contain the values
    int x, y, z;
    // collecting input from stdin
    cin >> x >> y >> z;
    // calculating the sum of values
    sum = x + y + z;
    // outputting the sum to stdout
    cout << sum << endl;

    return 0;
}
